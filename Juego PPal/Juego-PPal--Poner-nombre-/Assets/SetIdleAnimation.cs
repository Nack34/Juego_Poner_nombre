using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SetIdleAnimation : StateMachineBehaviour
{
    [SerializeField] private GameObject gameObject;
    private AnimationSelector animationSelector;
    [SerializeField] private float tiempoDeAnimacion=0.33f;
    [SerializeField] private float tiempoAnimando=0;

    // OnStateEnter is called before OnStateEnter is called on any state inside this state machine
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        animationSelector = gameObject.GetComponent<AnimationSelector>();
    }

    // OnStateUpdate is called before OnStateUpdate is called on any state inside this state machine
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        tiempoAnimando+=Time.deltaTime;
        if (tiempoAnimando>=tiempoDeAnimacion){
            tiempoAnimando=0;
            animator.SetBool(AnimationStrings.DejarDeAnimar,true);
        }
    }

